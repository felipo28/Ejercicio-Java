    private void TXT_salarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TXT_salarioKeyTyped
        int key = evt.getKeyChar();
        boolean numeros = key >= 48 && key <= 57;
        if (!numeros) {
            evt.consume();
        }
    }//GEN-LAST:event_TXT_salarioKeyTyped

    private void TXT_DiasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TXT_DiasKeyTyped
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;

        if (!numeros) {
            evt.consume();
        }
        if (TXT_Dias.getText().trim().length() == 10) {
            evt.consume();
        }
        if (TXT_Dias.getText().length() >= 3) {
            evt.consume();
        }
    }//GEN-LAST:event_TXT_DiasKeyTyped

