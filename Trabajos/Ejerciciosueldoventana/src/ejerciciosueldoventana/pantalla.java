    private void Boton_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Boton_calcularActionPerformed
        if (TXT_salario.getText().equals("") || TXT_Dias.getText().equals("")) {
            //error
            JOptionPane.showMessageDialog(rootPane, "Error. Los textos no pueden estar vacios", " errores", HEIGHT);

        } else {
            double salario, dias, cesantias, interesce, vacaciones, prima, total;
            salario = Double.parseDouble(TXT_salario.getText());
            dias = Double.parseDouble(TXT_Dias.getText());
            
            if (salario >= 1000000) {
                if (dias>=1) {
                    if (dias <= 360) {
                        String resul;

                        cesantias = (salario * dias) / 360;

                        interesce = (cesantias * 12) / 100;
                        vacaciones = (salario * dias) / 720;
                        prima = (salario * dias) / 720;

                        total = cesantias + interesce + vacaciones + prima;

                        TXT_cesantias.setText(String.valueOf(cesantias));
                        TXT_Interes_cesan.setText(String.valueOf(interesce));
                        TXT_Vacaciones.setText(String.valueOf(vacaciones));
                        TXT_Prima.setText(String.valueOf(prima));
                        TXT_Liquidacion.setText(String.valueOf(total));

                        TXT_Liquidacion.setText(String.valueOf(total));
                        resul = "Salario: " + salario + "\n Dias Trabajados: " + dias + "\n Cesantias: " + cesantias + "\n Interes: " + interesce + "\n Vacaciones: " + vacaciones
                                + "\n Prima: " + prima + "\n TOTAL: " + total;
                        TextoArea.setText(resul);
                        
                        model.addElement(resul);
                        jList1.setModel(model);
                    } else {
                        System.out.println("Los dias no puede ser mayor a 360.");
                        TXT_cesantias.setText("");
                        TXT_Interes_cesan.setText("");
                        TXT_Vacaciones.setText("");
                        TXT_Prima.setText("");
                        TXT_Liquidacion.setText("");
                        TextoArea.setText("");
                    }
                } else {
                    System.out.println("Los dias no puede ser menor a 1.");
                    TXT_cesantias.setText("");
                    TXT_Interes_cesan.setText("");
                    TXT_Vacaciones.setText("");
                    TXT_Prima.setText("");
                    TXT_Liquidacion.setText("");
                    TextoArea.setText("");
                }
                
            } else {
                System.out.println("El salario no puede ser menor al salario minimo.");
                TXT_cesantias.setText("");
                TXT_Interes_cesan.setText("");
                TXT_Vacaciones.setText("");
                TXT_Prima.setText("");
                TXT_Liquidacion.setText("");
                TextoArea.setText("");
            }

        }
    }//GEN-LAST:event_Boton_calcularActionPerformed

